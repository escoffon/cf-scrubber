module Cf::Scrubber::USDA
  # A namespace for US Forest Service (USDA's US Forest Service) helper utilities.
  #
  # == Forest descriptors.
  # Forest descriptors are hashes that contain information about a national forest, grassland, or
  # recreation area, which are collectively referred to as _forests_.
  # These hashes contain the following key/value pairs:
  # - *:name* The forest name.
  # - *:remap* The name of another descriptor to use for this forest; see below.
  # - *:ridb_id* The RIDB identifier of the forest.
  # - *:usfs_id* The USFS identifier for the forest.
  # - *:label* If this is a RIDB rec area, this is the identifier for its home page in the USFS web site;
  #   this value is extracted from rec area URL.
  #   For example, Tahoe National Forest's URL is +http://www.fs.usda.gov/tahoe+, and the label is
  #   +tahoe+. The North Carolina umbrella rec area's URL is +http://www.fs.usda.gov/nfsnc+, and its
  #   label is +nfsnc+ (North Carolina national forests are stored in RIDB as facilities of the
  #   <tt>National Forests in North Carolina</tt> umbrella rec area.
  # - *:container_ridb_id* If this forest is a facility of a container rec area, this is the RIDB
  #   identifier of that rec area.
  # - *:container_usfs_id* If this forest is a facility of a container rec area, this is the USFS
  #   identifier of that rec area.
  # - *:container_label* If this forest is a facility of a container rec area, this is the
  #   identifier of that rec area on the USFS web site.
  # - *:url* The URL to the forest's home page.
  #
  # === Remapped forest descriptors
  # Some forest names in the USFS web site are not consistent with the ones in the RIDB; for
  # example, North Carolina's <tt>William B. Bankhead National Forest</tt> is accessed by that name in the
  # USFS web site, but as <tt>Bankhead National Forest</tt> in RIDB. The descriptor associated with
  # the former name contains a *:remap* entry with value <tt>Bankhead National Forest</tt>, whose
  # descriptor contains the forest data.
  #
  # Some states organize some or all of their national forests under "umbrella" rec area; for example,
  # <tt>Arapaho National Forest</tt>, <tt>Roosevelt National Forest</tt>, and
  # <tt>Pawnee National Grassland</tt> in Colorado are accessed on the USFS web site through the
  # <tt>Arapaho & Roosevelt National Forests Pawnee NG</tt> rec area. In that case, the various forests
  # are organized in RIDB as facilities of the "umbrella" rec area, and each descriptor contains
  # a *:remap* property pointing at the "umbrella" rec area.

  module USFSHelper
    # The USFS web site's host.

    USFS_WEB_SITE_HOST = 'www.fs.usda.gov'

    # @!visibility private

    USFS_WEB_SITE_RE = /www\.fs\.(usda|fed)\.(us|gov)\/(.*)/i

    # @!visibility private

    USFS_LABEL_RE = /www\.fs\.(usda|fed)\.(us|gov)\/([a-z]+)/i

    # Get the built-in registry of RIDB rec areas that <i>look like</i> they are national forests.
    # (Or national grasslands).
    # This method returns the registry as generated by 
    # the {Cf::Scrubber::RIDB::Script::PrintNationalForests} script.
    #
    # @return [Hash] Returns a hash whose keys are two-letter state codes (as symbols), and values
    #  the RIDB raw data for rec areas that appear to describe USFS national forests or grasslands.

    def self.ridb_national_forests()
      RIDB_NATIONAL_FORESTS
    end

    # Get the built-in registry of USFS national forests and grasslands associated with each state.
    # This method returns the registry as generated by 
    # the {Cf::Scrubber::USDA::Script::PrintForestsPerState} script.
    #
    # @return [Hash] Returns a hash whose keys are two-letter state codes (as symbols), and values
    #  are arays of strings listing the names of national forests and grasslands that the USFS web site
    #  associates with the state. Note that some names appear in multiple states.

    def self.usfs_state_forests()
      USFS_STATE_FORESTS
    end

    # Get the built-in registry of USFS national forests and grasslands.
    # This method returns the registry as generated by 
    # the {Cf::Scrubber::USDA::Script::PrintNationalForests} script.
    #
    # @return [Hash] Returns a hash whose keys are forest or grassland names (as strings), and values
    #  the associated USFS descriptors. This registry includes "umbrella" rec areas, and tracks the
    #  overall organization of data in the USFS web site.

    def self.usfs_national_forests()
      USFS_NATIONAL_FORESTS
    end

    # Given a URL, check if it fits the pattern for a national forest's home page.
    # The home page URLs for national forests or grasslands have the form 
    # <tt>&lt;USFS root&gt;/&lt;label&gt;</tt>, where <tt>&lt;USFS root&gt;</tt> is the address of the
    # USFS web site, and <tt>&lt;label&gt;</tt> is a unique string that identifies the entity.
    #
    # @param url [String] The URL to check.
    #
    # @return [Boolean] Returns +true+ if _url_ fits the pattern, +false+ otherwise.

    def self.forest_home_url?(url)
      url && (url =~ USFS_WEB_SITE_RE)
    end

    # Given a USFS URL for a national forest's home page, extract the associated label.
    # The home page URLs for national forests or grasslands have the form 
    # <tt>&lt;USFS root&gt;/&lt;label&gt;</tt>, where <tt>&lt;USFS root&gt;</tt> is the address of the
    # USFS web site, and <tt>&lt;label&gt;</tt> is a unique string that identifies the entity.
    # For example, the label for <tt>Tahoe National Forest</tt> is +tahoe+, and for the "umbrella"
    # rec area <tt>National Forests in North Carolina</tt> it is +nfsnc+.
    #
    # This method extracts the label from the URL, if the URL fits the pattern described above.
    #
    # @param url [String] The URL to check.
    #
    # @return [String,nil] Returns a string containing the label; if _url_ does not fit the pattern,
    #  returns +nil+.

    def self.extract_label(url)
      (url && (url =~ USFS_WEB_SITE_RE)) ? Regexp.last_match[3] : nil
    end

    # Generate a list of forest descriptors from forest names.
    # This method iterates over the national forest or grassland names listed in _flist_, and looks them
    # up in the module's registry of known forest/grassland names. If a descriptor by that name is found,
    # it then checks if it contains the property *:remap*, and if so it attempts to resolve that name.
    # This continues until either a descriptor is found with no *:remap* property, or no descriptor by
    # a given name is found. In the first case, the descriptor is pushed on a list of resolved descriptors.
    # In the second case, the resolution chain is pushed on a list of unresolved names.
    #
    # @param flist [String, Array<String>] An array of strings (or a string that is placed in an array)
    #  that lists the forest names to resolve.
    # @param usfs [Hash] A hash containing the forest registry; keys are forest names, and values are
    #  hashes containing forest descriptors. If +nil+, the module's internal registry
    #  (as returned by {.usfs_national_forests}) is used.
    #
    # @return [Array,nil] Returns a 2-element array that contains:
    #  0. An array of resolved descriptors. This array does not contain duplicates; for example, in
    #     Colorado <tt>Arapaho National Forest</tt>, <tt>Roosevelt National Forest</tt>, and
    #     <tt>Pawnee National Grassland</tt> are all resolved to the descriptor for
    #     <tt>Arapaho & Roosevelt National Forests Pawnee NG</tt> rec area.
    #     If _flist_ contains all three, only one descriptor is placed in the resolved list.
    #  1. An array of resolution chains listing the sequence of lookups that eventually failed to
    #     return a descriptor. For example, if <tt>Not a National Forest</tt> is in _flist_,
    #     the resolution chain is <code>[ 'Not a National Forest' ]</code>.
    #     If <tt>Top NF</tt> is present and remaps to the missing <tt>Child NF</tt>, then the resolution
    #     chain is <code>[ 'Top NF', 'Child NF' ]</code>.
    #     A nonempty array of resolution chains indicates that some names were not found in the
    #     name registry.

    def self.convert_forest_descriptors(flist, usfs = nil)
      usfs = (usfs) ? usfs : usfs_national_forests()
      return [ nil, nil ] if usfs.nil?

      flist = [ flist ] unless flist.is_a?(Array)

      descriptors = [ ]
      unresolved = [ ]
      seen = { }

      flist.each do |f|
        unless seen[f]
          dn, un = _resolve(f, usfs, seen)
          descriptors.concat(dn)
          unresolved.concat(un)
        end
      end

      [ descriptors, unresolved ]
    end

    private

    def self._resolve(name, usfs, seen)
      return [ [ ], [ ] ] if seen[name]

      desc = usfs[name]
      return [ [ ], [ [ name ] ] ] if desc.nil?

      seen[name] = desc
      return [ [ desc ], [ ] ] unless desc.has_key?(:remap)

      remap = desc[:remap].is_a?(Array) ? desc[:remap] : [ desc[:remap] ]
      descriptors = [ ]
      unresolved = [ ]

      remap.each do |r|
        u = [ name ]
        d = usfs[r]
        if d
          nd, nu = _resolve(r, usfs, seen)
          if nu.count > 0
            # at least some were not found: we need to add this to the list of unresolved

            u << r
            unresolved << u
          end

          descriptors.concat(nd)
          nd.each { |d| seen[d[:name]] = d }
        else
          # not found: we need to add this to the list of unresolved

          u << r
          unresolved << u
        end
      end

      [ descriptors, unresolved ]
    end

    public

    # List the names of all forests and grasslands associated with one or more states.
    # Uses the built-in registry of states and forest names to build an aggregate list.
    #
    # @param states [String, Symbol, Array[String, Symbol>] The list of states for which to fetch
    #  names. A string or symbol is converted to a one-element array. The names are two-letter codes.
    # @param usfs [Hash] A custom registry of state names and forest lists. If +nil+, the built-in
    #  registry is used.
    #
    # @return [Array<String>] Returns an array containing the list of names.

    def self.forests_for_state(states, usfs = nil)
      usfs = (usfs.is_a?(Hash)) ? usfs : usfs_state_forests()
      slist = (states.is_a?(Array)) ? states : [ states ]

      slist.reduce([ ]) do |rv, s|
        l = usfs[s.to_s.upcase.to_sym]
        rv |= l unless l.nil?
        rv
      end
    end

    # Generate a list of forest descriptors associated with one or more states.
    # This method calls {.forests_for_state} to build the list of forest names, and then
    # {.convert_forest_descriptors} to generate the list of descriptors.
    #
    # @param states [String, Symbol, Array[String, Symbol>] The list of states for which to fetch
    #  names. A string or symbol is converted to a one-element array. The names are two-letter codes.
    # @param usfs [Hash] A hash containing the forest registry; keys are forest names, and values are
    #  hashes containing forest descriptors. If +nil+, the module's internal registry
    #  (as returned by {.usfs_national_forests}) is used.
    #
    # @return [Hash] Returns the value returned by the call to {.convert_forest_descriptors}.

    def self.forest_descriptors_for_state(states, usfs = nil)
      convert_forest_descriptors(forests_for_state(states, usfs), usfs)
    end

    # Get the list of RIDB forest descriptors for a state.
    #
    # @param state [Symbol,String] The two-letter code for a state (also supports territory codes
    #  like +AS+ for American Samoa).
    # @param ridb [Hash] A hash containing raw national forest data as extracted from RIDB.
    #  The keys are state codes (+AK+, +CA+, +NV+, end so on), and the values are hashes containing
    #  descriptors for (what seem to be) national forests or grasslands in the RIDB.
    #  Currently, these hashes contain a single key/value pair entry, with key *:rec_areas*; its
    #  value is an array of RIDB rec areas that were identified as national forests or grasslands
    #  by the {#Cf::Scrubber::RIDB::Script::ListNationalForests} script.
    #
    # @return [Array<Hash>, nil] Returns an array of hashes that list the national forests or grasslands
    #  in _state_. See the module documentation for a description of the contents of these hashes.
    #  Returns +nil+ if _state_ is not a supported state code.

    def self.ridb_forest_descriptors_for_state(state, ridb = nil)
      ridb = (ridb) ? ridb : RIDB_NATIONAL_FORESTS
      return nil if ridb.nil?

      state_rec = ridb[state.upcase.to_sym]
      return nil if state_rec.nil?

      rec_areas = state_rec[:rec_areas]
      return nil if rec_areas.nil?

      flist = [ ]

      # The RIDB contains some duplicates (for example, AL lists Talladega NF twice), and the dump
      # tool does not try to remove them, so we will do it here based on the URLs

      urls = { }

      rec_areas.each do |ra|
        # let's extract id and label

        container_ridb_id = (ra[:ridb_id]) ? ra[:ridb_id] : nil
        container_usfs_id = (ra[:usfs_id]) ? ra[:usfs_id] : nil
        container_label = extract_label(ra[:url])

        # OK, so first the top level rec area.
        # Typically, either the rec area is a forest, or it is an umbrella designation.
        # We assume that, if :facilities is nonempty, the rec area is an umbrella designation, and
        # we do not place it in the list. To force placement, set :include to true in the descriptor.

        if (ra[:facilities].length < 1) || ra[:include]
          url = ra[:url]
          if url
            h = {
              name: ra[:name],
              url: url,
              shared: ra[:shared]
            }
            h[:ridb_id] = container_ridb_id if container_ridb_id
            h[:usfs_id] = container_usfs_id if container_usfs_id
            h[:label] = container_label if container_label
            flist << h
            urls[url] = true
          end
        end

        # now the facilities. We ignore the ones marked :ignore, and we skip facilities with no URL,
        # or duplicate records

        if ra.has_key?(:facilities)
          ra[:facilities].each do |rf|
            unless rf[:ignore]
              url = rf[:url]
              if url && !urls[url]
                h = {
                  facility: true,
                  name: rf[:name],
                  url: url,
                  shared: rf[:shared]
                }
                h[:ridb_id] = rf[:ridb_id] if rf[:ridb_id]
                h[:usfs_id] = rf[:usfs_id] if rf[:usfs_id]
                h[:container_ridb_id] = container_ridb_id if container_ridb_id
                h[:container_usfs_id] = container_usfs_id if container_usfs_id
                h[:container_label] = container_label if container_label
                flist << h
                urls[url] = true
              end
            end
          end
        end
      end

      return flist
    end

